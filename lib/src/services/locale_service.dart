import 'package:flutter/material.dart';
import 'package:shared_preferences/shared_preferences.dart';
import 'package:scalable_short_video_app/src/services/api_service.dart';
import 'package:scalable_short_video_app/src/services/auth_service.dart';

class LocaleService extends ChangeNotifier {
  static final LocaleService _instance = LocaleService._internal();
  factory LocaleService() => _instance;
  LocaleService._internal() {
    print('üåê LocaleService._internal() constructor called - registering listeners');
    _authService.addLogoutListener(_onLogout);
    _authService.addLoginListener(_onLogin);
    print('‚úÖ LocaleService listeners registered');
  }

  final ApiService _apiService = ApiService();
  final AuthService _authService = AuthService();

  String _currentLocale = 'vi';
  
  String get currentLocale => _currentLocale;
  bool get isVietnamese => _currentLocale == 'vi';
  bool get isEnglish => _currentLocale == 'en';

  void _onLogin() {
    // Load language from backend when user logs in
    print('üë§ Login detected in LocaleService - loading language from backend');
    _loadLanguageFromBackend().catchError((error) {
      print('‚ùå Error in _onLogin while loading language: $error');
    });
  }

  void _onLogout() {
    // Reset to Vietnamese when user logs out
    print('üåê Logout detected - resetting to Vietnamese');
    _currentLocale = 'vi';
    SharedPreferences.getInstance().then((prefs) async {
      await prefs.setString('app_locale', 'vi');
      print('üíæ Vietnamese locale saved to storage');
    });
    notifyListeners();
    print('üì¢ Locale listeners notified - currentLocale: $_currentLocale');
  }

  Future<void> init() async {
    final prefs = await SharedPreferences.getInstance();
    _currentLocale = prefs.getString('app_locale') ?? 'vi';
    print('üåê LocaleService initialized - local locale: $_currentLocale');
    notifyListeners();
  }

  Future<void> _loadLanguageFromBackend() async {
    try {
      final token = await _authService.getToken();
      if (token == null) {
        print('‚ö†Ô∏è No token found, skipping backend language load');
        return;
      }

      print('üîÑ Loading language from backend...');
      final response = await _apiService.getUserSettings(token);
      print('üì¶ Backend language response: $response');
      
      if (response['success'] == true && response['settings'] != null) {
        final language = response['settings']['language'] as String?;
        if (language != null && language.isNotEmpty) {
          final wasLocale = _currentLocale;
          _currentLocale = language;
          
          // Save to local storage
          final prefs = await SharedPreferences.getInstance();
          await prefs.setString('app_locale', language);
          
          print('‚úÖ Language loaded from backend: $language (changed from $wasLocale to $_currentLocale)');
          
          // Notify listeners if locale changed
          if (wasLocale != _currentLocale) {
            print('üì¢ Locale changed - notifying listeners');
            notifyListeners();
          }
        }
      }
    } catch (e, stackTrace) {
      print('‚ö†Ô∏è Failed to load language from backend: $e');
      print('Stack trace: $stackTrace');
    }
  }

  /// Load language from backend settings (called after login)
  Future<void> loadFromBackend(String? language) async {
    if (language != null && language.isNotEmpty && language != _currentLocale) {
      _currentLocale = language;
      final prefs = await SharedPreferences.getInstance();
      await prefs.setString('app_locale', language);
      notifyListeners();
      print('‚úÖ LocaleService: Loaded language from backend: $language');
    }
  }

  Future<void> setLocale(String locale) async {
    if (_currentLocale == locale) return;
    
    _currentLocale = locale;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString('app_locale', locale);
    
    // Sync to backend if user is logged in
    if (_authService.isLoggedIn) {
      _syncLanguageToBackend(locale);
    }
    
    notifyListeners();
  }

  Future<void> _syncLanguageToBackend(String language) async {
    try {
      final token = await _authService.getToken();
      if (token == null) return;

      await _apiService.updateUserSettings(token, {
        'language': language,
      });
      print('‚úÖ Language synced to backend: $language');
    } catch (e) {
      print('‚ö†Ô∏è Failed to sync language to backend: $e');
    }
  }

  String get(String key) {
    final translations = _currentLocale == 'vi' ? _viTranslations : _enTranslations;
    return translations[key] ?? key;
  }

  /// Alias for get() method - for convenience
  String translate(String key) => get(key);

  /// Get current locale as Locale object
  Locale get currentLocaleObject => Locale(_currentLocale);

